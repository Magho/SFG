package sample;

public class PosXY {
    private double x,y;

    public PosXY(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public PosXY() {
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
